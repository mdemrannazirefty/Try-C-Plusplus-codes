#include<iostream>
using namespace std;

int main()
{
    /*Int SUM*/
    int a,b,sum;

    cout<< "Your 1st number: ";
    cin>> a;

    cout<< "Your 2nd number: ";
    cin>> b;

    sum = a+b;
    cout<< "your sum is: " <<sum <<endl ;

    /*Float or double SUM*/

    float c,d,s;

    cout<< "Your 1st number: ";
    cin>> c;

    cout<< "Your 2nd number: ";
    cin>> d;

    s = c+d;
    cout<< "your sum is: " <<s <<endl ;




    return 0;
}
